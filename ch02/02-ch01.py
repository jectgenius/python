str2 = input("문자열 2: ") # 표준 입력 함수 input() 호출하여 입력 안내 메세지 출력 동시에 사용자로부터 입력 받은 문자열을 리턴하여 변수 str2에 대입
print(str1, str2) # 표준 출력 함수 print() 호출하여 변수 str1, str2에 저장된 문자열 출력, 인자가 여러개인 경우 ,콤마로 구분하여 전달, 그러면 한칸씩 띄어쓰기되어 출력
