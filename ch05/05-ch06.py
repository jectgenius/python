    for j in range(len(m)): # 변수 j가 0부터 리스트 m의 요소의 개수 미만까지의 정수가 순서대로 모두 할당될 동안 반복
        print(m[j][i], end=" ") # 행 바뀜, 열 표준 출력, 함수 print() 호출하여 m[j][i] 요소 출력, 기본매개변수 end의 인자가 " "이므로 한 칸씩 띄어서 출력
    print()
